    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplication.LaunchOptionsKey: Any]?) -> Bool {
        configurarAudio()
        return true
    }

    /// Faz o som tocar mesmo com a chavinha lateral no silencioso.
    ///
    /// Por padrão o WKWebView usa a categoria `soloAmbient`, que respeita o botão
    /// físico de mudo — é por isso que, no Safari, o jogo fica mudo com a chavinha
    /// ligada, e não há como contornar isso pela web. A categoria `.playback` é a
    /// usada por apps de música/jogos e ignora o mudo, que é o comportamento
    /// esperado de um jogo com trilha e efeitos.
    ///
    /// `.mixWithOthers` é deliberado: se o jogador já estiver ouvindo música em
    /// outro app, o Bela Climb não interrompe a música dele.
    private func configurarAudio() {
        do {
            try AVAudioSession.sharedInstance().setCategory(.playback, options: [.mixWithOthers])
            try AVAudioSession.sharedInstance().setActive(true)
        } catch {
            // Falha ao configurar o áudio não pode derrubar o app: o jogo segue
            // normalmente, apenas respeitando o mudo como antes.
            print("AVAudioSession: \(error.localizedDescription)")
        }
    }

